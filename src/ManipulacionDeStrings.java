public class ManipulacionDeStrings {
    public static void main(String[] args) {

        String nombre = "Daniel";

        System.out.println(nombre.length()); /// longitud de cadena
        System.out.println(nombre.toLowerCase()); /// Transformar a minusculas
        System.out.println(nombre.equals("Dickson")); /// Comparar objeto String con otro
        System.out.println(nombre.charAt(2)); // Mostrar un caracter en determinada posicion
        System.out.println(nombre.substring(4));


    }
}
